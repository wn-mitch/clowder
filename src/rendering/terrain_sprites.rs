const CENTER_FILL_INDEX: u32 = 46;
const DECORATIVE_BASE: u32 = 47;
const DECORATIVE_COUNT: u32 = 12;

/// Maps 8-bit blob bitmask to grass overlay atlas index.
pub fn grass_overlay_atlas_index(bitmask: u8) -> u32 {
    BLOB_TO_ATLAS[bitmask as usize]
}

/// Maps blob bitmask to atlas index, with decorative center fill variants.
/// For center fill tiles (bitmask 255), uses a position-based hash to select
/// one of 12 decorative variants with a ~30% chance.
pub fn grass_overlay_atlas_index_with_variant(bitmask: u8, x: i32, y: i32) -> u32 {
    let base = BLOB_TO_ATLAS[bitmask as usize];
    if base == CENTER_FILL_INDEX {
        let hash = (x as u32)
            .wrapping_mul(7)
            .wrapping_add((y as u32).wrapping_mul(13));
        if hash % 10 < 3 {
            DECORATIVE_BASE + (hash / 10) % DECORATIVE_COUNT
        } else {
            base
        }
    } else {
        base
    }
}

/// Base layer tile index for terrain.
pub fn base_tile_index(terrain: &Terrain) -> u32 {
    match terrain.group() {
        TerrainGroup::Grass | TerrainGroup::Special => 0,
        TerrainGroup::Water => 1,
        TerrainGroup::Dirt => 2,
        TerrainGroup::Sand => 3,
        TerrainGroup::Rock => 4,
        TerrainGroup::Stone => 5,
        TerrainGroup::Building => 6,
    }
}

/// Whether this terrain should have a grass overlay rendered on top.
pub fn has_grass_overlay(terrain: &Terrain) -> bool {
    matches!(terrain.group(), TerrainGroup::Grass | TerrainGroup::Special)
}


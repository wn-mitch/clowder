#[cfg(test)]
mod tests {
    use super::*;

    fn make_map(terrain: &[&[Terrain]]) -> TileMap {
        let height = terrain.len() as i32;
        let width = terrain[0].len() as i32;
        let mut map = TileMap::new(width, height, Terrain::Water);
        for (y, row) in terrain.iter().enumerate() {
            for (x, &t) in row.iter().enumerate() {
                map.set(x as i32, y as i32, t);
            }
        }
        map
    }

    #[test]
    fn surrounded_grass_has_full_bitmask() {
        let map = make_map(&[
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
        ]);
        assert_eq!(blob_bitmask(&map, 1, 1, TerrainGroup::Grass), 255);
    }

    #[test]
    fn isolated_grass_has_zero_bitmask() {
        let map = make_map(&[
            &[Terrain::Water, Terrain::Water, Terrain::Water],
            &[Terrain::Water, Terrain::Grass, Terrain::Water],
            &[Terrain::Water, Terrain::Water, Terrain::Water],
        ]);
        assert_eq!(blob_bitmask(&map, 1, 1, TerrainGroup::Grass), 0);
    }

    #[test]
    fn diagonal_only_set_when_both_cardinals_present() {
        // NE grass present, but only N cardinal — NE bit should NOT be set
        let map = make_map(&[
            &[Terrain::Water, Terrain::Grass, Terrain::Grass],
            &[Terrain::Water, Terrain::Grass, Terrain::Water],
            &[Terrain::Water, Terrain::Water, Terrain::Water],
        ]);
        let mask = blob_bitmask(&map, 1, 1, TerrainGroup::Grass);
        assert_eq!(mask & 1, 1, "N should be set");
        assert_eq!(mask & 2, 0, "NE should NOT be set (E cardinal absent)");
    }

    #[test]
    fn diagonal_set_when_both_cardinals_present() {
        // N, E, and NE all grass — NE bit should be set
        let map = make_map(&[
            &[Terrain::Water, Terrain::Grass, Terrain::Grass],
            &[Terrain::Water, Terrain::Grass, Terrain::Grass],
            &[Terrain::Water, Terrain::Water, Terrain::Water],
        ]);
        let mask = blob_bitmask(&map, 1, 1, TerrainGroup::Grass);
        assert_eq!(mask & 1, 1, "N set");
        assert_eq!(mask & 2, 2, "NE set");
        assert_eq!(mask & 4, 4, "E set");
    }

    #[test]
    fn out_of_bounds_counts_as_same_group() {
        let map = make_map(&[&[Terrain::Grass]]);
        assert_eq!(blob_bitmask(&map, 0, 0, TerrainGroup::Grass), 255);
    }

    #[test]
    fn north_edge_bitmask() {
        // North is water, rest is grass — should produce N edge (124)
        let map = make_map(&[
            &[Terrain::Water, Terrain::Water, Terrain::Water],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
        ]);
        // E(4) + SE(8) + S(16) + SW(32) + W(64) = 124
        assert_eq!(blob_bitmask(&map, 1, 1, TerrainGroup::Grass), 124);
    }

    #[test]
    fn inner_corner_bitmask() {
        // All neighbors grass except diagonal NE — should produce NE inner corner
        let map = make_map(&[
            &[Terrain::Grass, Terrain::Grass, Terrain::Water],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
        ]);
        // All cardinal + SE(8) + SW(32) + W... wait let me compute
        // N(1) + NE(0, NE is water) + E(4) + SE(8) + S(16) + SW(32) + W(64) + NW(128)
        // = 1+4+8+16+32+64+128 = 253
        assert_eq!(blob_bitmask(&map, 1, 1, TerrainGroup::Grass), 253);
    }

    #[test]
    fn atlas_lookup_covers_all_47_values() {
        let valid_bitmasks: Vec<u8> = vec![
            0, 1, 4, 5, 7, 16, 17, 20, 21, 23, 28, 29, 31, 64, 65, 68, 69, 71, 80, 81, 84, 85, 87,
            92, 93, 95, 112, 113, 116, 117, 119, 124, 125, 127, 193, 197, 199, 209, 213, 215, 221,
            223, 241, 245, 247, 253, 255,
        ];
        assert_eq!(valid_bitmasks.len(), 47);
        let mut atlas_indices: Vec<u32> = valid_bitmasks
            .iter()
            .map(|&m| grass_overlay_atlas_index(m))
            .collect();
        atlas_indices.sort();
        atlas_indices.dedup();
        assert_eq!(
            atlas_indices.len(),
            47,
            "all 47 should map to distinct indices"
        );
        assert_eq!(*atlas_indices.last().unwrap(), 46);
    }

    #[test]
    fn decorative_variant_stable_for_same_position() {
        let a = grass_overlay_atlas_index_with_variant(255, 5, 3);
        let b = grass_overlay_atlas_index_with_variant(255, 5, 3);
        assert_eq!(a, b);
    }

    #[test]
    fn decorative_variant_only_for_center_fill() {
        // Non-center bitmask should never return a decorative index
        let idx = grass_overlay_atlas_index_with_variant(124, 5, 3);
        assert!(idx < 47);
    }

    #[test]
    fn dirt_treats_grass_as_friendly() {
        // Mud tile surrounded by grass should get full bitmask (not isolated)
        let map = make_map(&[
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
            &[Terrain::Grass, Terrain::Mud, Terrain::Grass],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
        ]);
        assert_eq!(blob_bitmask(&map, 1, 1, TerrainGroup::Dirt), 255);
    }

    #[test]
    fn dirt_at_water_boundary_has_edge() {
        // Mud between water (north) and grass (south) — should show north edge
        let map = make_map(&[
            &[Terrain::Water, Terrain::Water, Terrain::Water],
            &[Terrain::Grass, Terrain::Mud, Terrain::Grass],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
        ]);
        let mask = blob_bitmask(&map, 1, 1, TerrainGroup::Dirt);
        assert_eq!(mask & 1, 0, "N should NOT be set (water is not friendly)");
        assert_eq!(mask & 4, 4, "E should be set (grass is friendly)");
        assert_eq!(mask & 16, 16, "S should be set (grass is friendly)");
    }

    #[test]
    fn grass_treats_dirt_as_friendly() {
        // Grass defers to Dirt's own overlay — no grass edge toward mud
        let map = make_map(&[
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
            &[Terrain::Mud, Terrain::Grass, Terrain::Grass],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
        ]);
        let mask = blob_bitmask(&map, 1, 1, TerrainGroup::Grass);
        assert_eq!(
            mask & 64,
            64,
            "W should be set (grass defers to dirt's overlay)"
        );
    }

    #[test]
    fn grass_not_friendly_to_water() {
        // Water has no overlay, so grass renders its own edge
        let map = make_map(&[
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
            &[Terrain::Water, Terrain::Grass, Terrain::Grass],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
        ]);
        let mask = blob_bitmask(&map, 1, 1, TerrainGroup::Grass);
        assert_eq!(mask & 64, 0, "W should NOT be set (water has no overlay)");
    }

    #[test]
    fn grass_renders_edge_toward_stone() {
        // Stone has its own overlay at z=2, but grass (z=3) should render
        // its scalloped edge on top — not defer with a flat full-fill.
        let map = make_map(&[
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
            &[Terrain::Wall, Terrain::Grass, Terrain::Grass],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
        ]);
        let mask = blob_bitmask(&map, 1, 1, TerrainGroup::Grass);
        assert_eq!(
            mask & 64,
            0,
            "W should NOT be set (grass renders edge toward stone)"
        );
    }

    #[test]
    fn grass_renders_edge_toward_rock() {
        let map = make_map(&[
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
            &[Terrain::Rock, Terrain::Grass, Terrain::Grass],
            &[Terrain::Grass, Terrain::Grass, Terrain::Grass],
        ]);
        let mask = blob_bitmask(&map, 1, 1, TerrainGroup::Grass);
        assert_eq!(
            mask & 64,
            0,
            "W should NOT be set (grass renders edge toward rock)"
        );
    }

    #[test]
    fn forest_counts_as_grass_group() {
        assert_eq!(Terrain::LightForest.group(), TerrainGroup::Grass);
        assert_eq!(Terrain::DenseForest.group(), TerrainGroup::Grass);
        assert_eq!(Terrain::Garden.group(), TerrainGroup::Grass);
    }

    #[test]
    fn terrain_group_classification() {
        assert_eq!(Terrain::Water.group(), TerrainGroup::Water);
        assert_eq!(Terrain::Rock.group(), TerrainGroup::Rock);
        assert_eq!(Terrain::Den.group(), TerrainGroup::Building);
        assert_eq!(Terrain::Wall.group(), TerrainGroup::Stone);
        assert_eq!(Terrain::Sand.group(), TerrainGroup::Sand);
    }
}

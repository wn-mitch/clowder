use crate::resources::map::{Terrain, TileMap};

/// Terrain rendering category — determines which sprite set to use.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum TerrainGroup {
    Grass,
    Water,
    Dirt,
    Sand,
    Rock,
    Stone,
    Building,
    Special,
}

impl Terrain {
    pub fn group(&self) -> TerrainGroup {
        match self {
            Terrain::Grass | Terrain::LightForest | Terrain::DenseForest | Terrain::Garden => {
                TerrainGroup::Grass
            }
            Terrain::Water => TerrainGroup::Water,
            Terrain::Mud => TerrainGroup::Dirt,
            Terrain::Sand => TerrainGroup::Sand,
            Terrain::Rock => TerrainGroup::Rock,
            Terrain::Den
            | Terrain::Hearth
            | Terrain::Kitchen
            | Terrain::Stores
            | Terrain::Workshop => TerrainGroup::Building,
            Terrain::Wall | Terrain::Gate | Terrain::Watchtower | Terrain::WardPost => {
                TerrainGroup::Stone
            }
            Terrain::FairyRing
            | Terrain::StandingStone
            | Terrain::DeepPool
            | Terrain::AncientRuin => TerrainGroup::Special,
        }
    }
}

/// Whether a neighbor counts as "same" for overlay bitmask calculation.
/// Some terrain groups treat adjacent groups as friendly so their overlays
/// blend smoothly rather than producing isolated edge artifacts.
fn is_friendly(tile_group: TerrainGroup, neighbor_group: TerrainGroup) -> bool {
    if tile_group == neighbor_group {
        return true;
    }
    match tile_group {
        // Dirt/Sand blend into Grass (grass overlay at higher z covers the overlap)
        TerrainGroup::Dirt | TerrainGroup::Sand => neighbor_group == TerrainGroup::Grass,
        // Rock and Stone blend into each other
        TerrainGroup::Rock => neighbor_group == TerrainGroup::Stone,
        TerrainGroup::Stone => neighbor_group == TerrainGroup::Rock,
        // Grass defers to Dirt/Sand (symmetric full-fill, clean transition)
        // but renders scalloped edges toward Rock/Stone/Water/Building.
        // The grass overlay at z=3 sits above stone (z=2), so the grass
        // edges naturally layer over the stone edges.
        TerrainGroup::Grass => matches!(neighbor_group, TerrainGroup::Dirt | TerrainGroup::Sand),
        _ => false,
    }
}

/// 8-bit blob bitmask for all 8 neighbors. Diagonal bits are only set when
/// both adjacent cardinal neighbors are also present.
///
/// Bit layout (clockwise): N=1, NE=2, E=4, SE=8, S=16, SW=32, W=64, NW=128
///
/// This produces exactly 47 unique reduced values from the 256 possible raw
/// combinations. Each maps to a distinct autotile sprite.
pub fn blob_bitmask(map: &TileMap, x: i32, y: i32, group: TerrainGroup) -> u8 {
    let same = |dx: i32, dy: i32| -> bool {
        let nx = x + dx;
        let ny = y + dy;
        if !map.in_bounds(nx, ny) {
            return true;
        }
        is_friendly(group, map.get(nx, ny).terrain.group())
    };
    // TileMap is Y-down: visual north = y-1, visual south = y+1.
    let n = same(0, -1);
    let e = same(1, 0);
    let s = same(0, 1);
    let w = same(-1, 0);

    let mut mask = 0u8;
    if n {
        mask |= 1;
    }
    if n && e && same(1, -1) {
        mask |= 2;
    } // NE
    if e {
        mask |= 4;
    }
    if e && s && same(1, 1) {
        mask |= 8;
    } // SE
    if s {
        mask |= 16;
    }
    if s && w && same(-1, 1) {
        mask |= 32;
    } // SW
    if w {
        mask |= 64;
    }
    if w && n && same(-1, -1) {
        mask |= 128;
    } // NW
    mask
}

/// 4-bit bitmask for cardinal neighbors only (kept for non-blob terrain types).
/// Bit layout: N=0x1, E=0x2, S=0x4, W=0x8
pub fn cardinal_bitmask(map: &TileMap, x: i32, y: i32, group: TerrainGroup) -> u8 {
    let mut mask = 0u8;
    let same = |dx: i32, dy: i32| -> bool {
        let nx = x + dx;
        let ny = y + dy;
        if !map.in_bounds(nx, ny) {
            return true;
        }
        map.get(nx, ny).terrain.group() == group
    };
    if same(0, -1) {
        mask |= 0x1;
    }
    if same(1, 0) {
        mask |= 0x2;
    }
    if same(0, 1) {
        mask |= 0x4;
    }
    if same(-1, 0) {
        mask |= 0x8;
    }
    mask
}

